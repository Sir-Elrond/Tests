<A NAME="loyalty"></A><H3><center>Loyalty Points</center></H3>

<center>Loyalty Points can be acquired by defeating the bosses of the Tower of Bosses each boss has his reward which would be Loyalty Points! Know the benefits of these points below.</center><BR><BR>

<b>Loyalty Points </b><BR>
The Loyalty Points will grant you ranks every time you increase your Loyalty Points amount these ranks benefit a lot since for each 360 Loyalty Points the character will receive a 5% bonus on all his skill points, however, the bonus that a character can receive is limited 50%.<BR><BR>

<b>Olympa Titles</b><BR>
