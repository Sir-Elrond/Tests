<b>Bonus en skill points</b><BR>
Characters with at least 360 Loyalty Points will receive a bonus in their skill points. Although the amount itself is not shown, a certain amount of points is needed to get your skill points level next. Like the experience level, an increasing amount of points is needed to get to the next skill as your skill points increase. For every 360 loyalty points a character receives a 5% bonus on all his skill points, however, the bonus that a character can receive is limited to 50%.<BR>
<br>
<b><i>To give an example:</b></i> A paladin needs 611 skill points for shielding skill 15. A character with 360 Loyalty Points has a bonus of 5%. This means that the character does not have 611 skill points but 611 + 5% / 611 = 641 skill points that remain the same as the shielding skill 15. A character with 2160 Loyalty Points and with that a bonus of 30% has 611 + 30% / 611 = 794 skill points that equal the shielding skill 16.<BR>
