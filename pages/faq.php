                        <li class="cd-faq-title"><h2>Client 12</h2></li>
                                <li>
                                    <a class="cd-faq-trigger" href="#0">Client 12 não abre? Error OpenGL?</a>
                                    <div class="cd-faq-content">
<p>
    <span>Siga os passos abaixo:</span>
    </p><ul>
        <li> Feche o client</li>
        <li> Abra o arquivo \/conf\/clientoptions.json usando algum editor de texto</li>
        <li> Pesquise (Ctrl + F) a palavra <strong>renderer</strong>, que será seguida de um número (ex: "renderer": 0)</li>
        <li> Troque o número para um valor entre 0 e 5, onde:</li>
    </ul><br>
    0 = AutoSelect<br>
    1 = OpenGL<br>
    3 = Software<br>
    4 = DirectX (Compatibility)<br>
    5 = DirectX (Performance)<br>
    5) Salve o arquivo (Ctrl + S) e tente abrir o client novamente
<p></p>
</div>
                                </li>
                                <li>
                                    <a class="cd-faq-trigger" href="#0">Como importar meus minimapas antigos?</a>
                                    <div class="cd-faq-content">Copie todo o conteúdo da pasta \packages\Tibia\minimap e cole na pasta do novo client.</div>
                                </li>
