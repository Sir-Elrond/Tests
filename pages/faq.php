                                    <a class="cd-faq-trigger" href="#0">Lost Password</a>
                                    <div class="cd-faq-content">
<h3>Password Lost (Access to Email)</h3><hr><p>If you need a new password, we can send you a link to request a new one.</p>
<p>Follow these steps, to request a new password:</p>
<ol>
<li>Use the <a href="?subtopic=lostaccount">Lost Account Interface</a> to request a new password. Enter the character name of the affected account.</li>
<li>Select "I&nbsp;have forgotten my password".</li>
<li>Enter the email address of the affected account.</li>
<li>Once you have submitted your request, we will send you an email with further instructions to the email address assigned to your Tibia account.</li>
<li>Click on the link provided in the email to get a new password. <em><br>If you do not log in with your new password within 24 hours, the password will be reset at the next server save to the old password. In this case, you can simply request a new password again.</em></li>
</ol>
<p><em><strong><img src="layouts/tibiacom/images/global/faq/faq_exclamation.gif" alt="">&nbsp;If you do not receive our email, please check the following:</strong></em></p>
<ul>
<li>Do you have enough space in your mailbox?</li>
<li>Check your spam filter and see if it identifies our emails as spam and blocks it.</li>
<li>Check your junk box -&gt; If you find it there, mark it as "No junk" or "No spam".</li>
</ul>
</div>
